package DadosCalculadora;

public class Jorge {
    private int valor1;
    private int valor2;
    private int soma;

    public Jorge(int valor1, int valor2, int soma) {
        this.valor1 = valor1;
        this.valor2 = valor2;
        this.soma = soma;
    }

    public int calcular(int valor1, int valor2){
        int resultado = valor1 + valor2;
        return this.soma = resultado;
    }

}
